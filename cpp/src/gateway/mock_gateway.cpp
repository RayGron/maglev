DeployRequestProposal MockGateway::request_deploy(
    const std::string&,
    const std::string& instruction,
    const RepositoryContext& repository) {
    std::istringstream stream(instruction);
    std::string segment;
    std::string host = "example-host";
    while (stream >> segment) {
        const bool looks_like_host =
            segment.find('.') != std::string::npos ||
            std::all_of(segment.begin(), segment.end(), [](unsigned char ch) { return std::isdigit(ch); });
        if (looks_like_host) {
            host = segment;
            break;
        }
    }

    DeployRequestProposal proposal;
    proposal.host = host;
    proposal.repo_path = repository.root_path;
    proposal.branch = repository.branch;
    proposal.restart_command = std::string("echo restart service here");
    return proposal;
}

TerminalReply MockGateway::terminal_reply(const std::string&, const std::string& task, const Json& details) {
    const auto mode = details.value("mode", std::string("status"));
    if (mode == "chat" && gateway_detail::is_greeting(task)) {
        return TerminalReply{"Hi. I can chat here, or you can use /task <text> to run the agent workflow."};
    }
    if (mode == "chat") {
        return TerminalReply{"Chat mode: " + task +
                             "\nUse /task <text> when you want planning, edits, checks, commit, push, or deploy."};
    }
    return TerminalReply{"Task '" + task + "': planning complete."};
}

